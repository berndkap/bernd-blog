@app.route('/register', methods=['GET', 'POST'])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            flash("You've already signed up with that email, log in instead!")
            return redirect(url_for('login'))

        plain_password = form.password.data
        hashed_password = generate_password_hash(plain_password, method='pbkdf2:sha256', salt_length=8)
        new_user = User(
            email=form.email.data,
            password=hashed_password,
            name=form.name.data
        )
        db.session.add(new_user)
        db.session.commit()

        login_user(new_user)
        return redirect(url_for("get_all_posts"))

    return render_template("register.html", form=form, current_user=current_user)


@app.route('/login', methods=['GET', 'POST'])
def login():
    form = LoginForm()

    if form.validate_on_submit():
        email_login = form.email.data
        user = User.query.filter_by(email=email_login).first()
        if not user:
            flash("User doesn't exist! Give in existing email address")
            return redirect("login")

        password_login = form.password.data
        password_saved = user.password

        if check_password_hash(password_saved, password_login):
            login_user(user)
            return redirect(url_for('get_all_posts'))
        else:
            flash("Your password is not correct, try again!")
            return redirect("login")

    return render_template("login.html", form=form, current_user=current_user)


